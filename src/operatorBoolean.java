public class operatorBoolean {
    public static void main(String[] args) {
//        BERIKUT INI ADALAH OPERASI BOOLEAN
        /* operasi boolean akan mengembalikan nilai kembalian true or false*/
        int a = 30;
        int b = 50;

        System.out.println(a < b);
        System.out.println(a > b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a != b);
        System.out.println(a == b);
    }
}
